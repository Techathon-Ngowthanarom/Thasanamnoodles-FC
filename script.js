function filterCategory(category) {
    const filteredNews = newsData.filter(news => news.category === category);
    displayNews(filteredNews);
    document.getElementById('categoryTitle').textContent = `${category} News`;
}

function showAllNews() {
    displayNews(newsData);
    document.getElementById('categoryTitle').textContent = 'All News';
}

function toggleSearch() {
    const searchContainer = document.getElementById('searchForm');
    const searchInput = document.getElementById('searchInput');
    searchContainer.classList.toggle('active');
    searchInput.focus();
}

function searchNews() {
    const searchContainer = document.getElementById('searchForm');
    const searchInput = document.getElementById('searchInput');
    const searchTerm = searchInput.value.toLowerCase();
    
    if (!searchContainer.classList.contains('active')) {
        searchContainer.classList.add('active');
        searchInput.focus();
        return;
    }

    const filteredNews = newsData.filter(news =>
        news.title.toLowerCase().includes(searchTerm) ||
        news.content.toLowerCase().includes(searchTerm)
    );
    displayNews(filteredNews);
    document.getElementById('categoryTitle').textContent = 'Search Results';
}

