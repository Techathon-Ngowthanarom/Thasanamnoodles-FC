function showAlert(type, message) {
    const alertContainer = document.getElementById('alertContainer');
    alertContainer.innerHTML = `
        <div class="alert alert-${type} ${type === 'success' ? 'alert-dismissible' : ''}">
            ${type === 'success' ? '<button type="button" class="btn-close" data-bs-dismiss="alert"></button>' : ''}
            <strong>${type === 'success' ? 'Success!' : 'Info!'}</strong> ${message}
        </div>
    `;
    if (type === 'danger') {
        setTimeout(() => {
            alertContainer.innerHTML = '';
        }, 3000);
    }
}

function updateLoginLink() {
    const loginNavItem = document.getElementById('loginNavItem');
    if (currentUser) {
        loginNavItem.innerHTML = `
            <a class="nav-link dropdown-toggle" href="#" id="loginLink" role="button" data-bs-toggle="dropdown">${currentUser}</a>
            <ul class="dropdown-menu dropdown-menu-end">
                <li><a class="dropdown-item" href="#" data-bs-toggle="modal" data-bs-target="#logoutModal">Logout</a></li>
                <li><a class="dropdown-item" href="#" onclick="showAboutUs()">About Us</a></li>
            </ul>
        `;
    } else {
        loginNavItem.innerHTML = `
            <a class="nav-link" href="#" id="loginLink" data-bs-toggle="modal" data-bs-target="#loginModal">Login</a>
        `;
    }
}

function handleLogin() {
    const username = document.getElementById('username').value;
    const password = document.getElementById('password').value;
    const errorDiv = document.getElementById('loginError');

    if (username === "admin" && password === "admin") {
        currentUser = username;
        errorDiv.style.display = 'none';
        document.getElementById('loginForm').reset();
        bootstrap.Modal.getInstance(document.getElementById('loginModal')).hide();
        showAlert('success', 'Login successful');
        updateLoginLink();
        displayWelcomeMessage();
    } else {
        errorDiv.style.display = 'block';
    }
}

function confirmLogout() {
    currentUser = null;
    bootstrap.Modal.getInstance(document.getElementById('logoutModal')).hide();
    showAlert('danger', 'You have logged out successfully.');
    updateLoginLink();
    displayWelcomeMessage();
    showAllNews();
}

function createBackgroundShapes() {
    const container = document.getElementById('background-shapes');
    const shapes = ['circle', 'square', 'triangle'];
    const maxShapes = 30;

    function createShape() {
        const shapeType = shapes[Math.floor(Math.random() * shapes.length)];
        const shape = document.createElement('div');
        shape.classList.add('shape', shapeType);

        const size = Math.random() * 10 + 10;
        if (shapeType !== 'triangle') {
            shape.style.width = `${size}px`;
            shape.style.height = `${size}px`;
        }

        shape.style.left = `${Math.random() * 100}vw`;
        const duration = Math.random() * 5 + 3;
        shape.style.animationDuration = `${duration}s`;

        container.appendChild(shape);

        setTimeout(() => {
            shape.remove();
        }, duration * 1000);
    }

    setInterval(() => {
        if (container.children.length < maxShapes) {
            createShape();
        }
    }, 300);
}

